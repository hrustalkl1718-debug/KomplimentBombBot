def create_gradient_background(width, height, top_color, bottom_color):
    base = Image.new('RGB', (width, height), top_color)
    top = Image.new('RGB', (width, height), bottom_color)
    mask = Image.new('L', (width, height))
    mask_data = []
    for y in range(height):
        for x in range(width):
            mask_data.append(int(255 * (y / height)))
    mask.putdata(mask_data)
    base.paste(top, (0, 0), mask)
    return base

def create_compliment_card(text, theme_idx=None):
    width, height = 800, 600
    
    if theme_idx is None:
        theme = random.choice(BACKGROUND_THEMES)
    else:
        theme = BACKGROUND_THEMES[theme_idx % len(BACKGROUND_THEMES)]
    
    img = create_gradient_background(width, height, theme["top"], theme["bottom"])
    draw = ImageDraw.Draw(img)
    
    overlay = Image.new('RGBA', (width - 80, height - 120), (*theme["text"], 30))
    img.paste(overlay, (40, 60), overlay)
    
    try:
        font_large = ImageFont.truetype("fonts/DejaVuSans.ttf", 48)
        font_small = ImageFont.truetype("fonts/DejaVuSans.ttf", 28)
    except Exception as e:
        logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–≥—Ä—É–∑–∏—Ç—å –∫–∞—Å—Ç–æ–º–Ω—ã–π —à—Ä–∏—Ñ—Ç: {e}")
        font_large = ImageFont.load_default(40)
        font_small = ImageFont.load_default(20)
    
    lines = textwrap.wrap(text, width=28)
    
    y_position = height // 2 - (len(lines) * 35)
    for line in lines:
        bbox = draw.textbbox((0, 0), line, font=font_large)
        text_width = bbox[2] - bbox[0]
        x_position = (width - text_width) // 2
        draw.text((x_position, y_position), line, fill=theme["text"], font=font_large)
        y_position += 60
    
    draw.text((width // 2 - 100, height - 60), "@SweetNoteBot", fill=theme["text"], font=font_small)
    
    bio = io.BytesIO()
    img.save(bio, 'PNG', quality=95)
    bio.seek(0)
    
    return bio

